# Let's learn varibales

# Variable is a container that holds a specific value.
# In Python Variables hold a special place as they are used to contain different datatypes


x = 123 # x is a variable that holds the value 123
print(x) # The output would be the value that x contains
print(type(x))


y = 32.57
print(y)
print(type(y)) # y holds a float value


z = [1,2,3,4,5,6]
print(z)
print(type(z)) # z holds a list

t = {1:"x",2:"y",3:"z"}
print(t)
print(type(t)) # t holds a dictionary